public class datatypes {
    public static void main(String []args){
        int a = 56;
        long x = 1234567L; // in long must and should you have to use L at the end
        short num3 = 4;
        byte num4 = 120;
        float num5 = 5.3f; // in float we have to mention f at the end 
        double num6 = 5.632;
        char c = 'K'; // for char must and should you have to use single quotes
        boolean b = true;
        int s = 0b101; // the int accept binary format
        int h = 0x7E; // it accept the hexadecimal values also
        System.out.println(h);
    }
}
